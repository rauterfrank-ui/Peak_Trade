def test_incremental_override_updates():
    """
    Test dass sukzessive Updates in auto.toml korrekt verarbeitet werden.
    Simuliert mehrere Promotion Loop Cycles.
    """
    with TemporaryDirectory() as tmpdir:
        config_path = Path(tmpdir) / "config.toml"
        config_path.write_text(
            """
[environment]
mode = "live"

[portfolio]
leverage = 1.0

[strategy]
trigger_delay = 10.0

[macro]
regime_weight = 0.0
""",
            encoding="utf-8",
        )

        auto_path = Path(tmpdir) / "auto.toml"

        # Cycle 1: Nur Leverage-Override
        auto_path.write_text(
            """
[auto_applied]
"portfolio.leverage" = 1.25
""",
            encoding="utf-8",
        )

        cfg1 = load_config_with_live_overrides(
            config_path, auto_overrides_path=auto_path
        )
        assert cfg1.get("portfolio.leverage") == 1.25
        assert cfg1.get("strategy.trigger_delay") == 10.0
        assert cfg1.get("macro.regime_weight") == 0.0

        # Cycle 2: Leverage + Trigger-Delay
        auto_path.write_text(
            """
[auto_applied]
"portfolio.leverage" = 1.5
"strategy.trigger_delay" = 8.0
""",
            encoding="utf-8",
        )

        cfg2 = load_config_with_live_overrides(
            config_path, auto_overrides_path=auto_path
        )
        assert cfg2.get("portfolio.leverage") == 1.5
        assert cfg2.get("strategy.trigger_delay") == 8.0
        assert cfg2.get("macro.regime_weight") == 0.0

        # Cycle 3: Alle drei Parameter
        auto_path.write_text(
            """
[auto_applied]
"portfolio.leverage" = 1.75
"strategy.trigger_delay" = 7.0
"macro.regime_weight" = 0.3
""",
            encoding="utf-8",
        )

        cfg3 = load_config_with_live_overrides(
            config_path, auto_overrides_path=auto_path
        )
        assert cfg3.get("portfolio.leverage") == 1.75
        assert cfg3.get("strategy.trigger_delay") == 7.0
        assert cfg3.get("macro.regime_weight") == 0.3


