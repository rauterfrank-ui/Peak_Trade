"""
Realistic scenario tests for Live-Overrides integration.

Tests the complete workflow from Promotion Loop writing auto.toml
to PeakConfig loading it in different environments.
"""

from pathlib import Path
from tempfile import TemporaryDirectory

import pytest

from src.core.peak_config import load_config_with_live_overrides


def test_complete_workflow_live_environment():
    """
    Test des kompletten Workflows:
    1. Promotion Loop schreibt auto.toml
    2. Live-Session lädt Config mit Overrides
    3. Parameter werden korrekt angewendet
    """
    with TemporaryDirectory() as tmpdir:
        # Schritt 1: Setup - Basis-Config erstellen (Paper-Defaults)
        config_path = Path(tmpdir) / "config.toml"
        config_path.write_text(
            """
# Basis-Config (Paper-Defaults)
[environment]
mode = "live"
enable_live_trading = true
live_dry_run_mode = true

[portfolio]
leverage = 1.0
max_position_size = 0.1

[strategy]
name = "ma_crossover"
trigger_delay = 10.0
fast_window = 20
slow_window = 50

[risk]
max_position = 0.1
stop_loss = 0.02

[macro]
regime_weight = 0.0
""",
            encoding="utf-8",
        )

        # Schritt 2: Promotion Loop schreibt Auto-Overrides (bounded_auto)
        auto_path = Path(tmpdir) / "auto.toml"
        auto_path.write_text(
            """
# Auto-generated by Promotion Loop v0 (bounded_auto)
[auto_applied]
"portfolio.leverage" = 1.75
"strategy.trigger_delay" = 8.0
"macro.regime_weight" = 0.35
"risk.max_position" = 0.12
""",
            encoding="utf-8",
        )

        # Schritt 3: Live-Session lädt Config
        cfg = load_config_with_live_overrides(
            config_path, auto_overrides_path=auto_path
        )

        # Schritt 4: Verifikation - Overrides wurden angewendet
        assert cfg.get("portfolio.leverage") == 1.75
        assert cfg.get("strategy.trigger_delay") == 8.0
        assert cfg.get("macro.regime_weight") == 0.35
        assert cfg.get("risk.max_position") == 0.12

        # Unveränderte Werte bleiben erhalten
        assert cfg.get("portfolio.max_position_size") == 0.1
        assert cfg.get("strategy.fast_window") == 20
        assert cfg.get("strategy.slow_window") == 50
        assert cfg.get("risk.stop_loss") == 0.02


