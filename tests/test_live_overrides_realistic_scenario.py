def test_deeply_nested_overrides():
    """Test dass tief verschachtelte Pfade korrekt funktionieren."""
    with TemporaryDirectory() as tmpdir:
        config_path = Path(tmpdir) / "config.toml"
        config_path.write_text(
            """
[environment]
mode = "live"

[portfolio.risk.limits]
max_position = 0.1
max_leverage = 2.0

[strategy.ma_crossover.params]
fast = 10
slow = 30

[live.execution.slippage]
model = "fixed"
value = 0.001
""",
            encoding="utf-8",
        )

        auto_path = Path(tmpdir) / "auto.toml"
        auto_path.write_text(
            """
[auto_applied]
"portfolio.risk.limits.max_position" = 0.12
"portfolio.risk.limits.max_leverage" = 2.5
"strategy.ma_crossover.params.fast" = 12
"live.execution.slippage.value" = 0.0015
""",
            encoding="utf-8",
        )

        cfg = load_config_with_live_overrides(
            config_path, auto_overrides_path=auto_path
        )

        assert cfg.get("portfolio.risk.limits.max_position") == 0.12
        assert cfg.get("portfolio.risk.limits.max_leverage") == 2.5
        assert cfg.get("strategy.ma_crossover.params.fast") == 12
        assert cfg.get("live.execution.slippage.value") == 0.0015
        # Unchanged
        assert cfg.get("strategy.ma_crossover.params.slow") == 30
        assert cfg.get("live.execution.slippage.model") == "fixed"
